public class WelcomeMethod {
    
    // 主程式：程式執行的起點
    public static void main(String[] args) {
        // 呼叫下方定義的方法
        printWelcome();
    }

    // 定義一個名為 printWelcome 的方法
    public static void printWelcome() {
        System.out.println("Welcome to Java");
        System.out.println("Practice methods");
    }
    
}