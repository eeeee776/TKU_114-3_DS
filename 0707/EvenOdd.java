public class EvenOdd {
    public static void main(String[] args) {
        // 設定一個整數 number
        int number = 7; // 您可以更改這個數字來測試不同的結果

        // 判斷是偶數還是奇數
        if (number % 2 == 0) {
            System.out.println(number + " 是偶數");
        } else {
            System.out.println(number + " 是奇數");
        }
    }
}